package com.wty.entity.H5entity;

public class Card {
    private String card_id;
    private String card_time;
    private String card_endtime;


    public String getCard_id() {
        return card_id;
    }

    public void setCard_id(String card_id) {
        this.card_id = card_id;
    }

    public String getCard_time() {
        return card_time;
    }

    public void setCard_time(String card_time) {
        this.card_time = card_time;
    }

    public String getCard_endtime() {
        return card_endtime;
    }

    public void setCard_endtime(String card_endtime) {
        this.card_endtime = card_endtime;
    }
}
